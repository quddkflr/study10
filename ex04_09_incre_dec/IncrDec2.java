package ex04_09_incre_dec;

public class IncrDec2 {
	public static void main(String args[]) {
		int n1 = 10, n2 = 10, n3 = 10, n4 = 10;
		
		System.out.println(++n1);
		
		System.out.println(n1);
		
		System.out.println(n2++);
		
		System.out.println(n2);
		
		System.out.println(--n3);
		
		System.out.println(n3);
		
		System.out.println(n4--);
		
		System.out.println(n4);
	}

}
