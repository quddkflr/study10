package ex05_05_for;

public class ForExample4_3dan2 {
	public static void main(String args[]) {
		
		int dan =2 , b;			
		System.out.println("["+dan+"단]");
	
		for ( b = 1; b <= 9;b++ ) {
			System.out.println(dan+"*"+b+"="+dan*b);
			
			
			
		}
		dan=3		;	
		System.out.println("["+dan+"단]");
	
		for ( b = 1; b <= 9;b++ ) {
			System.out.println(dan+"*"+b+"="+dan*b);
			
			
			
		}
		dan=4		;	
		System.out.println("["+dan+"단]");
	
		for ( b = 1; b <= 9;b++ ) {
			System.out.println(dan+"*"+b+"="+dan*b);
			
			
			
		}
		
	}

}
